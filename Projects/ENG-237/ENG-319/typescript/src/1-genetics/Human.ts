/**
 * Represents the essential functions of a human being.
 */
export interface Human {
    /**
     * Pay attention to the sounds and speech in your surroundings.
     */
    hear(): void;
    /**
     * Engage in conversation and express yourself clearly and effectively.
     */
    speak(): void;
    /**
     * Observe your environment and take note of the visual information around you.
     */
    see(): void;
    /**
     * Use your sense of touch to interact with the world around you and feel sensations like heat, cold, or texture.
     */
    touch(): void;
    /**
     * Use your sense of smell to identify and differentiate various scents in your surroundings.
     */
    smell(): void;
    /**
     * Use your sense of taste to enjoy and differentiate the flavors of different foods and drinks.
     */
    taste(): void;
    /**
     * Use your muscles and motor skills to move and manipulate objects in your environment.
     */
    move(): void;
    /**
     * Use your cognitive abilities to think critically, analyze problems, and find solutions.
     */
    think(): void;
    /**
     * Seek out new information and actively engage in the learning process.
     */
    learn(): void;
    /**
     * Engage in creative activities and express yourself through art, music, writing, or other forms of self-expression.
     */
    create(): void;
    /**
     * Allow yourself to feel and express a range of emotions appropriately.
     */
    emote(): void;
    /**
     * Control your breathing and engage in activities that regulate your bodily functions.
     */
    breathe(): void;
    /**
     * Get enough sleep and rest to allow your body and mind to recharge and recover.
     */
    sleep(): void;
    /**
     * Engage in activities that lead to reproduction and passing on of genetic information.
     */
    reproduce(): void;
    /**
     * Engage in activities that allow you to interact with your surroundings and other living beings in a positive and meaningful way.
     */
    interact(): void;
  }